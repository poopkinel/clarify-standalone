// Context creation for global language state
export const useLanguage = () => {
  // Get stored language or default
  const [currentLanguage, setCurrentLanguage] = useState(() => {
    const storedLang = localStorage.getItem('language');
    return storedLang && AVAILABLE_LANGUAGES[storedLang] ? storedLang : DEFAULT_LANGUAGE;
  });

  // Translation helper function with plural support
  const t = (key, options) => {
    const lang = translations[currentLanguage] || translations[DEFAULT_LANGUAGE];
    let translation = lang[key] || translations[DEFAULT_LANGUAGE][key] || key;
    
    // If we have a count option and the translation is an object with plural forms
    if (options && typeof options.count !== 'undefined' && 
        typeof translation === 'object' && 
        (translation.one || translation.other)) {
      
      // Hebrew has special plural rules
      if (currentLanguage === 'he') {
        const count = options.count;
        if (count === 0 && translation.zero) return translation.zero.replace('{{count}}', count);
        if (count === 1 && translation.one) return translation.one.replace('{{count}}', count);
        if (count === 2 && translation.two) return translation.two.replace('{{count}}', count);
        if (count >= 3 && count <= 10 && translation.few) return translation.few.replace('{{count}}', count);
        return (translation.other || translation.one || String(translation)).replace('{{count}}', count);
      }
      
      // Simple English singular/plural
      translation = options.count === 1 ? 
        (translation.one || translation.other) : 
        (translation.other || translation.one);
    }
    
    // Ensure translation is a string
    translation = String(translation);
    
    // Handle string replacements
    if (options) {
      Object.keys(options).forEach(key => {
        translation = translation.replace(
          new RegExp(`{{${key}}}`, 'g'), 
          String(options[key])
        );
      });
    }
    
    return translation;
  };

  // Get current language direction (RTL or LTR)
  const direction = AVAILABLE_LANGUAGES[currentLanguage]?.direction || 'ltr';

  // Change language function
  const changeLanguage = (langCode) => {
    if (AVAILABLE_LANGUAGES[langCode]) {
      setCurrentLanguage(langCode);
      localStorage.setItem('language', langCode);
      
      // Update document direction for RTL/LTR support
      document.documentElement.dir = AVAILABLE_LANGUAGES[langCode].direction;
      document.documentElement.lang = langCode;
    }
  };

  // Set document direction on initial load
  useEffect(() => {
    document.documentElement.dir = direction;
    document.documentElement.lang = currentLanguage;
  }, [direction, currentLanguage]);

  return {
    currentLanguage,
    direction,
    t,
    changeLanguage,
    availableLanguages: AVAILABLE_LANGUAGES
  };
};
